#include <fcntl.h>
#include <unistd.h>
#include <sys/types.h>
#include <sys/stat.h>

int main(void)
{
	char *path = "/tmp/myfile.txt";
	int fd = creat(path, S_IRWXU);

	if (fd >= 0)
		close(fd);
	unlink(path);
	return 0;
}
